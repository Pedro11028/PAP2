<div id="organizarInformacoesDoSite">
    <h3>Objetivos do site</h3>
        O principal objetivo deste site é permitir com que o utilizador possa obter novos conhecimentos em diversas áreas. 
        O utilizador também pode partilhar o seu conhecimento ao criar quizzes interessantes e divertidos.<br>
    
    <br><br>
    
    <h3>Quizzes</h3>
        Cada quizz têm duas opções, selecionar resposta (o utilizador seleciona a resposta que achar correta), ou texto livre.
        O tipo "selecionar resposta" pode ter duas opções, uma em que mostra se o utilizador acertou ou não e outra que mostra a percentagem de pessoas que selecionaram esse quizz.<br>
    
    <br>

    <div class="divH4">
        <h4>Como jogar ou publicar um quizz</h4>
            Um utilizador deve primeiro criar uma conta e depois caso queira jogar ou criar um quizz o mesmo deve estar dentro da conta criada.<br>    
        <br>
        <h4>Avaliações</h4>
            Quando um utilizador concluir um quizz o utilizador pode avaliar o mesmo ao escrever um texto e atribuir uma nota de 1 a 5.
            Depois as avaliações ficam disponíveis para a visualização dos outros utilizadores, os mesmos podem dar gostos ou não gostos nas avaliações.
        <br>
    </div>
    
    <br><br>

    <h3>Pesquisas</h3>
        Para pesquisar não é necessário entrar dentro de uma conta, portanto sinta-se á vontade para esplorar os quizzes existentes.<br>
   
    <br><br>

    <h3>Perfil do Utilizador</h3>
        No perfil de utilizador é possível verificar e alterar os dados do mesmo, sendo estes; imagem de perfil, nome do utilizador (primeiro e último nome), nome único e password. <br>
        Existem duas opções para a imagem de perfil, ou utiliza os gifs disponibilizados pelo site que são obtidos através de um certo nível ou pode selecionar uma imagem que tenha guardado.
    
    <br><br>
    
    <div class="divH4">
        <h4>Sistema de níveis</h4>
            Cada utilizador tem um nível que é determinado pela quantidade de pontos obtidos, sendo que cada nível corresponde a 100 pontos.
            Para obter os pontos é necessário fazer um quizz, cada quizz pode fornecer no máximo 100 pontos sendo que a quantidade de pontos obtida depende do número de questões, ou seja caso um quizz tenha 5 questões então cada questão fornecerá 20 pontos (100/5).<br>
